package org.apache.wss4j.dom.validate;


import javax.xml.namespace.QName;

import org.apache.wss4j.common.dom.validate.Credential;
import org.apache.wss4j.common.ext.WSSecurityException;
import org.apache.wss4j.dom.handler.RequestData;

/**
 * This class does not do any Validation at all.
 */
public class NoOpValidator implements Validator {

    /**
     * Validate the credential argument.
     *
     * @param credential the Credential to be validated
     * @param data the RequestData associated with the request
     * @throws WSSecurityException on a failed validation
     */
    public Credential validate(Credential credential, RequestData data) throws WSSecurityException {
        return credential;
    }

    @Override
    public QName[] getSupportedQNames() {
        // TODO Auto-generated method stub
        throw new UnsupportedOperationException("Unimplemented method 'getSupportedQNames'");
    }

}
